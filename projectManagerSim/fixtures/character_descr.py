def get_character_seed_data():
    """Return deep-copy-like dicts safe for Character.objects.create()."""
    return [dict(character) for character in CHARACTER_ARCHETYPES]


def get_relationship_score(pk_a, pk_b):
    key = tuple(sorted((pk_a, pk_b)))
    return RELATIONSHIP_SCORES.get(key, 0)


def iter_relationship_seed_rows():
    """Yield template relationship rows using the canonical fixture scores."""
    for (pk_a, pk_b), score in sorted(RELATIONSHIP_SCORES.items()):
        yield {
            "character_a_pk": pk_a,
            "character_b_pk": pk_b,
            "relationship_score": score,
        }
